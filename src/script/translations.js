// Función helper para obtener traducciones en JavaScript dinámico
function t(key) {
    const lang = localStorage.getItem('language') || 'es';
    return translations[lang][key] || translations['es'][key] || key;
}

// Función para obtener el idioma actual
function getCurrentLanguage() {
    return localStorage.getItem('language') || 'es';
}

// Función para establecer el idioma
function setLanguage(lang) {
    localStorage.setItem('language', lang);
    applyTranslations();
    updateLanguageSelectors(lang);
}

// Función para aplicar las traducciones al DOM
function applyTranslations() {
    const lang = getCurrentLanguage();
    const t = translations[lang];

    if (!t) return;

    // Traducir elementos con data-i18n
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (t[key]) {
            element.textContent = t[key];
        }
    });

    // Traducir placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (t[key]) {
            element.placeholder = t[key];
        }
    });

    // Traducir títulos (title attribute)
    document.querySelectorAll('[data-i18n-title]').forEach(element => {
        const key = element.getAttribute('data-i18n-title');
        if (t[key]) {
            element.title = t[key];
        }
    });

    // Actualizar el atributo lang del html
    document.documentElement.lang = lang === 'es' ? 'es' : 'en';
}

// Función para sincronizar los selectores de idioma
function updateLanguageSelectors(lang) {
    const value = lang === 'es' ? 'ES' : 'EN';

    const idiomaDesktop = document.getElementById('idioma');
    const idiomaMobile = document.getElementById('idioma_movil');
    const idiomaConfig = document.getElementById('idioma-config');

    if (idiomaDesktop) idiomaDesktop.value = value;
    if (idiomaMobile) idiomaMobile.value = value;
    if (idiomaConfig) idiomaConfig.value = lang;
}

// Inicializar el sistema de idiomas cuando el DOM esté listo
document.addEventListener('DOMContentLoaded', function () {
    const currentLang = getCurrentLanguage();

    // Aplicar traducciones al cargar
    applyTranslations();
    updateLanguageSelectors(currentLang);

    // Añadir listeners a los selectores de idioma
    const idiomaDesktop = document.getElementById('idioma');
    const idiomaMobile = document.getElementById('idioma_movil');
    const idiomaConfig = document.getElementById('idioma-config');

    if (idiomaDesktop) {
        idiomaDesktop.addEventListener('change', function () {
            const lang = this.value === 'ES' ? 'es' : 'en';
            setLanguage(lang);
        });
    }

    if (idiomaMobile) {
        idiomaMobile.addEventListener('change', function () {
            const lang = this.value === 'ES' ? 'es' : 'en';
            setLanguage(lang);
        });
    }

    if (idiomaConfig) {
        idiomaConfig.addEventListener('change', function () {
            setLanguage(this.value);
        });
    }
});
